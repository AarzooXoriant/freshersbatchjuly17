function Bankdetails( _account_name,_account_bal,_account_number){
var account_name=_account_name;
var account_bal=_account_bal;
var account_number=_account_number;
	withdraw= function(amount)
	{
		account_bal=account_bal-amount;
		document.write(account_bal);
	},
	
	deposit= function(amount)
	{
		account_bal=account_bal+amount;
		document.write(account_bal);
	};
	return{
		with_draw: function(amt)
		{ 
		return(withdraw(amt));
		},
		depo_sit: function(amt)
		{ 
		return(deposit(amt));
		},
		name: function()
		{ 
		return(account_name);
		},
		bal: function()
		{ 
		return(account_bal);
		},
		number: function()
		{ 
		return(account_number);
		}
	}	
	
}


